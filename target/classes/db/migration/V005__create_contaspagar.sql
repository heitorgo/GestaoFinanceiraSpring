CREATE TABLE CONTAS_PAGAR(
    IDPAGAR BIGINT AUTO_INCREMENT NOT NULL,
    VALORPAGAR NUMERIC(11,2) NOT NULL,
    DATAPAGAR VARCHAR(15) NOT NULL,
    DETALHAMENTOPAGAR VARCHAR(60) NOT NULL,
    VERIFPAGAR BOOLEAN,
    IDEMPRESA BIGINT,	
    CONSTRAINT PK_PAGAR PRIMARY KEY (IDPAGAR),
    CONSTRAINT FK_EMPRESA_CONTASPAGAR  FOREIGN KEY (IDEMPRESA) REFERENCES empresas(IDEMPRESA) 
);