CREATE TABLE DESPESAS_FIXAS (
    IDDESPESAFIXA BIGINT AUTO_INCREMENT NOT NULL,
    VALORDESPESAFIXA NUMERIC(11,2) NOT NULL,
    FREQUENCIADESPESA VARCHAR(20) NOT NULL,
    DETALHAMENTODESPESAFIXA VARCHAR(60) NOT NULL,
    VERIFDESPESAFIXA BOOLEAN,
    IDEMPRESA BIGINT,	
    CONSTRAINT PK_DESPESAFIXA PRIMARY KEY(IDDESPESAFIXA),
    CONSTRAINT FK_EMPRESA_DESPESASFIXAS FOREIGN KEY(IDEMPRESA) REFERENCES empresas(IDEMPRESA)
);