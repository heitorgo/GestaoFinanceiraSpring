CREATE TABLE CONTAS_RECEBER (
    IDRECEBER BIGINT AUTO_INCREMENT NOT NULL,
    VALORRECEBER NUMERIC(11,2) NOT NULL,
    DATARECEBER VARCHAR(15) NOT NULL,
    DETALHAMENTORECEBER VARCHAR(60) NOT NULL,
    VERIFRECEBER BOOLEAN,
    IDEMPRESA BIGINT,	
    CONSTRAINT PK_RECEBER PRIMARY KEY(IDRECEBER),
    CONSTRAINT FK_EMPRESA_CONTASRECEBER FOREIGN KEY(IDEMPRESA) REFERENCES empresas(IDEMPRESA)
);