CREATE TABLE CUSTOS_FIXOS (
    IDCUSTOFIXO BIGINT AUTO_INCREMENT NOT NULL,
    VALORCUSTOFIXO NUMERIC(11,2) NOT NULL,
    FREQUENCIACUSTO VARCHAR(20) NOT NULL,
    DETALHAMENTOCUSTOFIXO VARCHAR(60) NOT NULL,
    VERIFCUSTOFIXO BOOLEAN,
    IDEMPRESA BIGINT,	
    CONSTRAINT PK_CUSTOFIXO PRIMARY KEY(IDCUSTOFIXO),
    CONSTRAINT FK_EMPRESA_CUSTOSFIXOS FOREIGN KEY(IDEMPRESA) REFERENCES empresas(IDEMPRESA)
);
