CREATE TABLE CUSTOS(
    IDCUSTO BIGINT AUTO_INCREMENT NOT NULL,
    VALORCUSTO NUMERIC(11,2) NOT NULL,
    DATACUSTO VARCHAR(15) NOT NULL,
    DETALHAMENTOCUSTO VARCHAR(60) NOT NULL,
    VERIFCUSTO BOOLEAN,
    IDEMPRESA BIGINT,	
    CONSTRAINT PK_CUSTO PRIMARY KEY(IDCUSTO),
    CONSTRAINT FK_EMPRESA_CUSTOS FOREIGN KEY(IDEMPRESA) REFERENCES empresas(IDEMPRESA)
);